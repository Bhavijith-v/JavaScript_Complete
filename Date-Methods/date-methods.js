let date = new Date();

console.log(date.toLocaleString()); // Output: 12/25/2023, 10:45:36 PM

console.log(date.toLocaleDateString()); // Output: 12/25/2023

console.log(date.toLocaleTimeString()); // Output: 10:45:36 PM

console.log(date.toISOString()); // Output: 2023-12-25T22:45:36.137Z

console.log(date.toTimeString()); // Output: 10:45:36 PM

console.log(date.toDateString()); // Output: Wed Dec 25 2023

console.log(date.getTimezoneOffset()); // Output: -21600

console.log(date.getMilliseconds()); // Output: 137

console.log(date.getSeconds()); // Output: 36

console.log(date.getMinutes()); // Output: 45

console.log(date.getHours()); // Output: 22

console.log(date.getDate()); // Output: 25

console.log(date.getMonth()); // Output: 11 (January is 0)

console.log(date.getFullYear()); // Output: 2025

console.log(date.getDay()); // Output: 3 (Wednesday is 3)

console.log(date.getUTCFullYear()); // Output: 2025

console.log(date.getUTCMonth()); // Output: 11

console.log(date.getUTCDate()); // Output: 25

console.log(date.getUTCHours()); // Output: 22

console.log(date.getMinutes()); // Output: 45

console.log(date.getUTCSeconds()); // Output: 36

console.log(date.getUTCMilliseconds()); // Output: 137



