let a = 10
let b = 5

let sum = a + b

console.log(sum) // Output: 15

let difference = a - b

console.log(difference) // Output: 5

let product = a * b

console.log(product) // Output: 50

let quotient = a / b

console.log(quotient) // Output: 2

let modulo = a % b

console.log(modulo) // Output: 0

//prefix increment 
//initailly a = 10
let increment = ++a

console.log(increment) // Output: 11



//postfix increment
//initially b = 5
let decrement = b++

console.log(decrement) // Output: 5



